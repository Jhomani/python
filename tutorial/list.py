myList = ["apple", "banana", "cherry"]
tropical = ["mango", "pineapple", "papaya"]

# Add List Items
myList.append("orange")   # add to end
myList.insert(1, "chee")   # add by index1
myList.extend(tropical)  # to join iterable object

# remove List Items
myList.remove("banana")  # remove via value
myList.pop(1) # remove via index or the last item
del myList[0] # remove via selector
del myList # remove all list
myList.clear() # to empties list




